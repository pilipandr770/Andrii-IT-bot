def get_text(lang_code, key, **kwargs):
    if lang_code not in TRANSLATIONS:
        lang_code = 'en'
    text = TRANSLATIONS[lang_code].get(key, TRANSLATIONS.get('en', {}).get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    return text

